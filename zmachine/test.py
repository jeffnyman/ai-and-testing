def collect_story_files(args: list[str]) -> list[Path]:
  """Expand any directory arguments to all zcode files within them."""
  files = []
  zcode_extensions = {".z1", ".z2", ".z3", ".z4", ".z5", ".z6", ".z7", ".z8"}

  for arg in args:
    p = Path(arg)
    if p.is_dir():
      found = sorted(f for f in p.iterdir() if f.suffix.lower() in zcode_extensions)
      if not found:
        print(f"[Warning] No zcode files found in directory: {p}")
      files.extend(found)
    elif p.exists():
      files.append(p)
    else:
      print(f"[Warning] File not found, skipping: {p}")

  return files


def main():
  if len(sys.argv) < 2:
    print(
      "Usage: python test_parser.py <story.z3> [story.z5 ...]\n"
      "       python test_parser.py <directory_of_zcode_files/>"
    )
    sys.exit(1)

  story_files = collect_story_files(sys.argv[1:])

  if not story_files:
    print("[Error] No story files to test.")
    sys.exit(1)

  print(f"[test_parser] Loading ontology from {TTL_FILE.name}...")
  g = load_ontology()
  print(f"[test_parser] Loaded {len(g)} triples.")

  print(f"[test_parser] Loading parser from {PARSER_FILE.name}...")
  parser_mod = load_parser()
  print("[test_parser] Parser loaded.")

  print(f"\n[test_parser] Testing {len(story_files)} file(s)...\n")

  results = []
  for story_path in story_files:
    result = test_file(story_path, g, parser_mod)
    print_result(result)
    results.append(result)

  print_summary_table(results)


if __name__ == "__main__":
  main()
