# Ontology queries
# ---------------------------------------------------------------------------


def version_number(g: Graph, version_uri: URIRef) -> int:
  """Return the integer version number for a ZMachineVersion individual."""
  for val in g.objects(version_uri, zm("versionNumber")):
    return int(val)
  return 0


def fields_for_version(g: Graph, v: int) -> list[dict]:
  """
  Return all HeaderField (and FlagsRegister) individuals applicable for
  version v, excluding superseded fields.

  A field is applicable for version v if:
    - applicableFrom <= v
    - applicableUntil is absent OR applicableUntil >= v
  """
  results = []

  for field in g.subjects(RDF.type, zm("HeaderField")):
    # Also catches FlagsRegister (subclass), but we want to handle
    # FlagsRegister separately, so skip pure FlagsRegisters here
    # unless we want their offset too. We include them — bits are
    # checked separately.
    af_uri = next(g.objects(field, zm("applicableFrom")), None)
    au_uri = next(g.objects(field, zm("applicableUntil")), None)

    if af_uri is None:
      continue  # Malformed entry — skip

    af = version_number(g, af_uri)
    au = version_number(g, au_uri) if au_uri else 999

    if not (af <= v <= au):
      continue

    # Skip fields superseded by another (the superseding field will
    # appear as its own individual with its own applicableFrom)
    is_superseded = any(True for _ in g.subjects(zm("supersededBy"), field))
    if is_superseded:
      continue

    label = str(next(g.objects(field, RDFS.label), short(field)))
    offset = next(g.objects(field, zm("offset")), None)
    authority = next(g.objects(field, zm("hasSourceAuthority")), None)
    is_flags = (field, RDF.type, zm("FlagsRegister")) in g

    results.append(
      {
        "uri": field,
        "label": label,
        "offset": int(offset) if offset is not None else None,
        "normative": str(authority) == ZM + "normative",
        "is_flags": is_flags,
      }
    )

  return sorted(results, key=lambda x: x["offset"] or 0)


def bits_for_version(g: Graph, register_uri: URIRef, v: int) -> list[dict]:
  """
  Return all BitField individuals within a FlagsRegister that are
  applicable for version v.
  """
  results = []
  for bit in g.objects(register_uri, zm("containsBit")):
    af_uri = next(g.objects(bit, zm("applicableFrom")), None)
    au_uri = next(g.objects(bit, zm("applicableUntil")), None)

    if af_uri is None:
      continue

    af = version_number(g, af_uri)
    au = version_number(g, au_uri) if au_uri else 999

    if not (af <= v <= au):
      continue

    label = str(next(g.objects(bit, RDFS.label), short(bit)))
    pos = next(g.objects(bit, zm("bitPosition")), None)
    authority = next(g.objects(bit, zm("hasSourceAuthority")), None)

    results.append(
      {
        "uri": bit,
        "label": label,
        "bit_position": int(pos) if pos is not None else None,
        "normative": str(authority) == ZM + "normative",
      }
    )

  return sorted(results, key=lambda x: x["bit_position"] or 0)


def extension_fields_for_version(g: Graph, v: int) -> list[dict]:
  """Return all ExtensionField individuals applicable for version v."""
  results = []
  for ef in g.subjects(RDF.type, zm("ExtensionField")):
    af_uri = next(g.objects(ef, zm("applicableFrom")), None)
    if af_uri is None:
      continue
    af = version_number(g, af_uri)
    if af > v:
      continue

    label = str(next(g.objects(ef, RDFS.label), short(ef)))
    idx = next(g.objects(ef, zm("wordIndex")), None)
    results.append(
      {
        "uri": ef,
        "label": label,
        "word_index": int(idx) if idx is not None else None,
      }
    )

  return sorted(results, key=lambda x: x["word_index"] or 0)


def superseded_pairs(g: Graph) -> list[tuple]:
  """Return (superseded_label, superseding_label) pairs."""
  pairs = []
  for s, p, o in g.triples((None, zm("supersededBy"), None)):
    s_label = str(next(g.objects(s, RDFS.label), short(s)))
    o_label = str(next(g.objects(o, RDFS.label), short(o)))
    pairs.append((s_label, o_label))
  return pairs


# ---------------------------------------------------------------------------
# Tricky case checks
# These probe the specific version-conditional branching cases that reveal
# whether the parser genuinely used the ontology or just pattern-matched.
# ---------------------------------------------------------------------------


def check_flags1_split(parser_output: dict, v: int) -> list[str]:
  """
  Flags 1 at offset 0x01 has entirely different bit meanings in V1-3
  vs V4+. Check that the parser branched correctly.
  """
  issues = []
  flags1 = None

  # Parser may nest flags under various key names; try common ones
  for key in ("flags1", "flags_1", "Flags 1", "flags"):
    candidate = parser_output.get(key)
    if isinstance(candidate, dict):
      flags1 = candidate
      break

  # Also check inside a nested 'flags_registers' or similar group
  if flags1 is None:
    for val in parser_output.values():
      if isinstance(val, dict):
        for key in ("flags1", "flags_1", "Flags 1"):
          if key in val:
            flags1 = val[key]
            break

  if flags1 is None:
    issues.append("WARN: Could not locate Flags 1 in parser output — key name unclear")
    return issues

  if v <= 3:
    # Expect V1-3 bits: status line type (bit 1), split discs (bit 2),
    # status not available (bit 4), screen split (bit 5), var pitch (bit 6)
    v4_only_terms = ("bold", "italic", "fixed", "timed", "picture", "colour", "color")
    flat = str(flags1).lower()
    for term in v4_only_terms:
      if term in flat:
        issues.append(f"FAIL: Flags 1 contains V4+ term '{term}' but file is V{v}")
  else:
    # Expect V4+ bits: colours, bold, italic, fixed-space, timed input, etc.
    v13_only_terms = ("score/turns", "hours:min", "split across", "two disc")
    flat = str(flags1).lower()
    for term in v13_only_terms:
      if term in flat:
        issues.append(f"FAIL: Flags 1 contains V1-3 term '{term}' but file is V{v}")

  if not issues:
    issues.append("PASS: Flags 1 version split appears correct")

  return issues


def check_font_swap(parser_output: dict, v: int) -> list[str]:
  """
  At offsets 0x26/0x27, font width and height are swapped between V5
  and V6. The ontology models this with supersededBy. Check that the
  parser output uses the correct label for the correct version.
  """
  issues = []

  if v < 5:
    issues.append("SKIP: Font fields only present from V5")
    return issues

  # Flatten all string keys in the output for a broad search
  flat = str(parser_output).lower()

  # In V5: 0x26 should be width, 0x27 should be height
  # In V6: 0x26 should be height, 0x27 should be width
  # We look for both labels and flag if they appear in the wrong version
  if v == 5:
    if "font_width" in flat or "font width" in flat:
      issues.append("PASS: Font width label present in V5 output")
    else:
      issues.append("WARN: Could not confirm font width label in V5 output")
    if "font_height" in flat or "font height" in flat:
      issues.append("PASS: Font height label present in V5 output")
    else:
      issues.append("WARN: Could not confirm font height label in V5 output")

  if v == 6:
    # Both labels should appear but mapped to swapped offsets.
    # We can't easily check the offset mapping from the dict alone,
    # so we just confirm both are present and note the swap.
    if ("font_width" in flat or "font width" in flat) and (
      "font_height" in flat or "font height" in flat
    ):
      issues.append(
        "PASS: Both font dimension labels present in V6 output "
        "(manual check: 0x26=height, 0x27=width in V6)"
      )
    else:
      issues.append("WARN: One or both font dimension labels missing in V6 output")

  return issues


def check_extension_table(parser_output: dict, v: int) -> list[str]:
  """
  Extension table should be present for V5+ and absent (or gracefully
  handled) for V1-4.
  """
  issues = []

  flat_keys = set(str(k).lower() for k in parser_output.keys())
  has_ext = any(
    "extension" in k or "ext_" in k or "flags3" in k or "flags_3" in k
    for k in flat_keys
  )

  # Also check nested dicts
  for val in parser_output.values():
    if isinstance(val, dict):
      for k in val.keys():
        if "extension" in str(k).lower() or "flags3" in str(k).lower():
          has_ext = True

  if v < 5:
    if has_ext:
      issues.append(
        f"WARN: Extension table data present in V{v} output — "
        "should be absent or empty for pre-V5 files"
      )
    else:
      issues.append(f"PASS: No extension table in V{v} output (correct)")
  else:
    if has_ext:
      issues.append(f"PASS: Extension table present in V{v} output")
    else:
      issues.append(
        f"WARN: No extension table in V{v} output — "
        "expected for V5+ (may be absent if pointer is 0)"
      )

  return issues


def check_conventional_fields(parser_output: dict) -> list[str]:
  """
  Fields from Appendix B (release number, serial code, Inform version)
  should appear in the output, ideally under a 'conventional' key.
  """
  issues = []
  flat = str(parser_output).lower()

  conventional_terms = {
    "release": "release number (0x02)",
    "serial": "serial code (0x12)",
    "inform": "Inform compiler version (0x3C)",
  }

  has_conventional_key = "conventional" in flat

  for term, description in conventional_terms.items():
    if term in flat:
      issues.append(f"PASS: Conventional field present — {description}")
    else:
      issues.append(f"WARN: Conventional field missing — {description}")

  if has_conventional_key:
    issues.append("PASS: Output contains a 'conventional' grouping key")
  else:
    issues.append(
      "WARN: No 'conventional' grouping key — conventional fields "
      "may be mixed with normative fields"
    )

  return issues


