def _find_parse_function(mod):
  """
  Attempt to find the header-parsing callable in the generated module.

  Strategy (in order):
    1. Known conventional names.
    2. Any top-level callable whose name contains 'parse' or 'header'
       and whose annotation or default suggests it takes bytes.
    3. Any top-level callable that accepts exactly one positional argument,
       tested by calling it with a minimal 64-byte zeroed buffer to see
       if it returns a dict.

  Returns the callable, or None if nothing suitable is found.
  Also prints what was found to aid debugging.
  """
  import inspect

  # Step 1: conventional names
  candidates = [
    "parse_header",
    "parse",
    "read_header",
    "decode_header",
    "parse_zmachine_header",
    "read_zmachine_header",
  ]
  for name in candidates:
    fn = getattr(mod, name, None)
    if callable(fn):
      print(f"[test_parser] Found parse function by name: {name}")
      return fn

  # Step 2: name-based heuristic on all module-level callables
  for name, obj in inspect.getmembers(mod, inspect.isfunction):
    if any(kw in name.lower() for kw in ("parse", "header", "read", "decode")):
      print(f"[test_parser] Found parse function by name heuristic: {name}")
      return obj

  # Step 3: probe any single-argument callable with a dummy buffer
  dummy = bytes(64)
  for name, obj in inspect.getmembers(mod, inspect.isfunction):
    try:
      sig = inspect.signature(obj)
      params = [
        p for p in sig.parameters.values() if p.default is inspect.Parameter.empty
      ]
      if len(params) == 1:
        result = obj(dummy)
        if isinstance(result, dict):
          print(
            f"[test_parser] Found parse function by probe: {name} "
            f"(returns dict for dummy input)"
          )
          return obj
    except Exception:
      continue

  # Nothing found — report what is available
  all_fns = [name for name, _ in inspect.getmembers(mod, inspect.isfunction)]
  print(
    f"[test_parser] WARNING: Could not find a suitable parse function.\n"
    f"             Functions available in zmachine_header.py: {all_fns}\n"
    f"             Set PARSE_FUNCTION_NAME at the top of test_parser.py "
    f"to override.",
    file=sys.stderr,
  )
  return None


