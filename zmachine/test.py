"""
test.py

Tests the generated zmachine_header.py parser against real Z-Machine story
files. Uses the ontology (zmachine_header.ttl) as the ground truth for what
fields should be present for a given version, rather than hardcoding
expectations.

For each story file provided, the script:

  1. Reads the version byte directly from the file (bypassing the parser)
     to establish the ground truth version.
  2. Queries the ontology to determine which fields and bits are expected
     for that version.
  3. Runs the parser and collects its output.
  4. Compares expected vs. actual, reporting missing fields, unexpected
     fields, and known tricky cases (Flags 1 split, font field swap,
     extension table).
  5. Prints a per-file report and a final summary table.

Usage:
    python test.py path/to/story1.z3 path/to/story2.z5 ...

    Or to test all zcode files in a directory:
    python test.py path/to/zcode_dir/

Requirements:
    - rdflib  (pip install rdflib)
    - zmachine_header.py  in the same directory as this script
      (generated by generate.py)
    - zmachine_header.ttl  in the same directory as this script
"""

import importlib.util
import sys

# import struct
from pathlib import Path
# from collections import defaultdict

from rdflib import Graph, URIRef  # , Literal
from rdflib.namespace import RDF, RDFS

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

SCRIPT_DIR = Path(__file__).parent
TTL_FILE = SCRIPT_DIR / "zmachine_header.ttl"
PARSER_FILE = SCRIPT_DIR / "zmachine_header.py"

# If automatic function discovery fails, set this to the exact function
# name used in the generated zmachine_header.py, e.g. "read_header"
PARSE_FUNCTION_NAME = None

ZM = "http://example.org/zmachine/"


def zm(local):
  return URIRef(ZM + local)


def short(uri):
  return str(uri).replace(ZM, "zm:")


# ---------------------------------------------------------------------------
# Load the ontology
# ---------------------------------------------------------------------------


def load_ontology() -> Graph:
  if not TTL_FILE.exists():
    print(f"[Error] Ontology not found: {TTL_FILE}", file=sys.stderr)
    sys.exit(1)
  g = Graph()
  g.parse(str(TTL_FILE), format="turtle")
  return g


# ---------------------------------------------------------------------------
# Load the generated parser as a module
# ---------------------------------------------------------------------------


def load_parser():
  if not PARSER_FILE.exists():
    print(
      f"[Error] Parser not found: {PARSER_FILE}\n        Run generate_parser.py first.",
      file=sys.stderr,
    )
    sys.exit(1)
  spec = importlib.util.spec_from_file_location("zmachine_header", PARSER_FILE)
  if spec is None or spec.loader is None:
    print(f"[Error] Could not load module spec from {PARSER_FILE}", file=sys.stderr)
    sys.exit(1)
  mod = importlib.util.module_from_spec(spec)
  spec.loader.exec_module(mod)
  return mod


