def test_file(
  story_path: Path,
  g: Graph,
  parser_mod,
) -> dict:
  """
  Run all checks for a single story file.
  Returns a results dict with pass/warn/fail counts and per-check details.
  """
  result = {
    "path": story_path,
    "version": None,
    "parse_error": None,
    "field_coverage": [],
    "tricky_cases": [],
    "summary": {"pass": 0, "warn": 0, "fail": 0},
  }

  # --- Read raw bytes ---
  try:
    data = story_path.read_bytes()
  except OSError as e:
    result["parse_error"] = f"Could not read file: {e}"
    return result

  if len(data) < 64:
    result["parse_error"] = (
      f"File too short ({len(data)} bytes) — not a valid Z-Machine file"
    )
    return result

  # --- Determine version from raw bytes (ground truth) ---
  v = data[0]
  result["version"] = v

  if v < 1 or v > 6:
    result["parse_error"] = f"Unexpected version byte: {v}"
    return result

  # --- Run the parser ---
  parser_output = None
  try:
    # Allow manual override via module-level constant
    if PARSE_FUNCTION_NAME:
      parse_fn = getattr(parser_mod, PARSE_FUNCTION_NAME, None)
      if parse_fn is None:
        result["parse_error"] = (
          f"PARSE_FUNCTION_NAME='{PARSE_FUNCTION_NAME}' was set "
          f"but not found in zmachine_header.py"
        )
        return result
    else:
      parse_fn = _find_parse_function(parser_mod)

    if parse_fn is None:
      result["parse_error"] = (
        "Could not find a suitable parse function in zmachine_header.py. "
        "Set PARSE_FUNCTION_NAME at the top of test_parser.py to override."
      )
      return result

    parser_output = parse_fn(data)

  except Exception as e:
    result["parse_error"] = f"Parser raised exception: {type(e).__name__}: {e}"
    return result

  # --- Field coverage: ontology-derived expectations ---
  expected_fields = fields_for_version(g, v)
  flat_output = str(parser_output).lower()

  for field in expected_fields:
    label_lower = field["label"].lower()
    # Use key words from the label to search the output
    keywords = [
      w
      for w in label_lower.split()
      if len(w) > 3 and w not in ("field", "offset", "version", "table", "address")
    ]
    found = any(kw in flat_output for kw in keywords) if keywords else False
    authority = "normative" if field["normative"] else "conventional"
    status = "PASS" if found else "WARN"
    result["field_coverage"].append(
      f"{status} [{authority}] {field['label']} (offset 0x{field['offset']:02X})"
      if field["offset"] is not None
      else f"{status} [{authority}] {field['label']}"
    )

  # --- Tricky case checks ---
  result["tricky_cases"].extend(check_flags1_split(parser_output, v))
  result["tricky_cases"].extend(check_font_swap(parser_output, v))
  result["tricky_cases"].extend(check_extension_table(parser_output, v))
  result["tricky_cases"].extend(check_conventional_fields(parser_output))

  # --- Count pass/warn/fail ---
  all_checks = result["field_coverage"] + result["tricky_cases"]
  for line in all_checks:
    if line.startswith("PASS"):
      result["summary"]["pass"] += 1
    elif line.startswith("WARN"):
      result["summary"]["warn"] += 1
    elif line.startswith("FAIL"):
      result["summary"]["fail"] += 1

  return result


# ---------------------------------------------------------------------------
# Reporting
# ---------------------------------------------------------------------------


def print_result(result: dict):
  path = result["path"]
  v = result["version"]

  print()
  print("=" * 70)
  print(f"  File   : {path.name}")
  print(f"  Version: {v if v else '?'}")
  print("=" * 70)

  if result["parse_error"]:
    print(f"\n  [ERROR] {result['parse_error']}")
    return

  print("\n--- Field Coverage (ontology-derived expectations) ---")
  for line in result["field_coverage"]:
    print(f"  {line}")

  print("\n--- Tricky Case Checks ---")
  for line in result["tricky_cases"]:
    print(f"  {line}")

  s = result["summary"]
  print(f"\n  Summary: {s['pass']} PASS  |  {s['warn']} WARN  |  {s['fail']} FAIL")


def print_summary_table(results: list[dict]):
  print()
  print("=" * 70)
  print("  SUMMARY TABLE")
  print("=" * 70)
  print(f"  {'File':<30} {'V':>3}  {'PASS':>5}  {'WARN':>5}  {'FAIL':>5}  {'Status'}")
  print(f"  {'-' * 30}  {'-' * 3}  {'-' * 5}  {'-' * 5}  {'-' * 5}  {'-' * 10}")

  for r in results:
    name = r["path"].name[:30]
    v = str(r["version"]) if r["version"] else "?"
    if r["parse_error"]:
      print(f"  {name:<30} {v:>3}  {'':>5}  {'':>5}  {'':>5}  ERROR")
      continue
    s = r["summary"]
    status = (
      "OK"
      if s["fail"] == 0 and s["warn"] == 0
      else "WARN"
      if s["fail"] == 0
      else "FAIL"
    )
    print(
      f"  {name:<30} {v:>3}  {s['pass']:>5}  {s['warn']:>5}  {s['fail']:>5}  {status}"
    )

  print()
  versions_seen = sorted(set(r["version"] for r in results if r["version"]))
  print(f"  Versions tested: {versions_seen}")
  print(f"  Files tested   : {len(results)}")


